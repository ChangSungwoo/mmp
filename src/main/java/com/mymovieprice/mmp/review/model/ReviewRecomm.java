package com.mymovieprice.mmp.review.model;

public class ReviewRecomm {
	private String recommSeq;
	private String reviewSeq;
	private String movieId;
	private String userNo;
	private String recommDiv;
	private String recommUserNo;
	private String creId;
	private String creDt;
	public String getRecommSeq() {
		return recommSeq;
	}
	public void setRecommSeq(String recommSeq) {
		this.recommSeq = recommSeq;
	}
	public String getReviewSeq() {
		return reviewSeq;
	}
	public void setReviewSeq(String reviewSeq) {
		this.reviewSeq = reviewSeq;
	}
	public String getMovieId() {
		return movieId;
	}
	public void setMovieId(String movieId) {
		this.movieId = movieId;
	}
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getRecommDiv() {
		return recommDiv;
	}
	public void setRecommDiv(String recommDiv) {
		this.recommDiv = recommDiv;
	}
	public String getRecommUserNo() {
		return recommUserNo;
	}
	public void setRecommUserNo(String recommUserNo) {
		this.recommUserNo = recommUserNo;
	}
	public String getCreId() {
		return creId;
	}
	public void setCreId(String creId) {
		this.creId = creId;
	}
	public String getCreDt() {
		return creDt;
	}
	public void setCreDt(String creDt) {
		this.creDt = creDt;
	}
	
	
}
